// 날짜 문자열(YYYY-MM-DD)을 숫자 인덱스로 변환
function dateToIndex(dateStr: string, poolSize: number): number {
  // 날짜를 숫자로 변환 (YYYYMMDD)
  const n = parseInt(dateStr.replace(/-/g, ''), 10);
  return n % poolSize;
}

