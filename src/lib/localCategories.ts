/** 오늘 날짜 기준 로컬 카테고리 반환 */
export function getLocalCategoryForDate(dateStr: string): DailyCategory {
  const index = dateToIndex(dateStr, RAW_CATEGORIES.length);
  const raw = RAW_CATEGORIES[index];
  return {
    id: `local-${index}`,
    date: dateStr,
    name: raw.name,
    words: raw.words.map((word, i) => ({ id: `local-${index}-${i}`, word })),
  };
}
